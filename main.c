void readIntegersFromFile(const char* filename, int** numbers, int* size) {
    FILE *file = fopen(filename, "r");

    if (file == NULL) {
        perror("Error opening file");
        return;
    }

    int count = 0;
    int num;
    while (fscanf(file, "%d", &num) != EOF) {
        count++;
    }

    *numbers = (int*)malloc(count * sizeof(int));
    if (*numbers == NULL) {
        perror("Memory allocation failed");
        fclose(file);
        return;
    }

    fseek(file, 0, SEEK_SET);

    int index = 0;
    while (fscanf(file, "%d", &num) != EOF) {
        (*numbers)[index++] = num;
    }

    *size = count;

    fclose(file);
}
